#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;

	i = 0;
	dest = malloc (sizeof(const char) * (ft_strlen(s) + 1));
	if (!dest)
		return (NULL);
	if (!s && *s == '\0')
		return (0);
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
